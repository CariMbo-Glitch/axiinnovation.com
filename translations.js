/* ============================================================
   Language Engine
   ============================================================ */
function setLang(lang) {
  if (!AXI_LANG[lang]) lang = 'en';
  localStorage.setItem('axi_lang', lang);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = AXI_LANG[lang][key];
    if (val !== undefined) el.innerHTML = val;
  });

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  /* Update html lang attribute */
  const langMap = { en: 'en-US', pt: 'pt-BR', es: 'es-419', fr: 'fr-FR' };
  document.documentElement.lang = langMap[lang] || 'en-US';
}

document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('axi_lang') || 'en';
  setLang(saved);
});
